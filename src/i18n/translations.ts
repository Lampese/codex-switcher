export function isLanguage(value: unknown): value is Language {
  return (
    value === "en" ||
    value === "zh" ||
    value === "fr" ||
    value === "de" ||
    value === "el" ||
    value === "ru" ||
    value === "ja" ||
    value === "ko" ||
    value === "es"
  );
}

export function localeForLanguage(language: Language): string {
  switch (language) {
    case "zh":
      return "zh-CN";
    case "fr":
      return "fr-FR";
    case "de":
      return "de-DE";
    case "el":
      return "el-GR";
    case "ru":
      return "ru-RU";
    case "ja":
      return "ja-JP";
    case "ko":
      return "ko-KR";
    case "es":
      return "es-ES";
    default:
      return "en-US";
  }
}

export function translate(
  language: Language,
  key: string,
  params?: Record<string, string | number>
): string {
  const template = TRANSLATIONS[language][key] ?? key;

  if (!params) return template;
  return template.replace(/{{\s*([\w.]+)\s*}}/g, (_, token: string) => {
    const value = params[token];
    return value === undefined || value === null ? `{{${token}}}` : String(value);
  });
}
